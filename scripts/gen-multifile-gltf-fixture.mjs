#!/usr/bin/env node
// Generator for the multi-file glTF e2e fixtures (#110 Wave E Task 10).
//
// Wave F's importer e2e gates the ingestion + sibling-resolution + percent-
// encoding paths against REAL external `.gltf` + `.bin` + texture trios. Up
// to now only single-file `.glb` fixtures existed (gen-skinned-fixture.mjs,
// seed-sample-assets.mjs), which never exercise the per-uri OPFS-sibling
// walk in `opfsSiblingPath` / `loadMultiFileGltf`. This script generates
// three committed variants under `public/fixtures/multifile/`:
//
//   (1) flat/     — `.gltf` + `.bin` + `.png` at the folder root. Easy case.
//   (2) nested/   — `.gltf` lives in `nested/gltf/` and refers to siblings
//                   via `../buffers/` and `../textures/`. This is the C1
//                   trap (non-root entry, `../`-relative siblings).
//   (3) spaced/   — like flat/, but the texture filename contains a literal
//                   space on disk; the `.gltf` URI percent-encodes it as
//                   `%20`. Exercises the encode/decode boundary Task 11
//                   verifies (importer decodes, renderer assumes encoded).
//
// Why post-process instead of letting GLTFExporter own texture emission:
// GLTFExporter with `{ binary:false }` returns a JSON whose `buffers[0]` and
// `images[]` are data-URIs (embedded). The whole POINT of the multi-file
// fixture is EXTERNAL siblings — so we extract `buffers[0]`'s data-URI to
// `scene.bin`, and we inject an external `images[0]` referencing a PNG we
// write ourselves. The PNG is a hand-coded 67-byte canonical 1×1 red pixel
// (the smallest valid PNG): no `canvas` / `sharp` / `jimp` dependency.
//
// Run: `node scripts/gen-multifile-gltf-fixture.mjs`. Idempotent; commit the
// generated bytes (the script + the fixtures are both the source of truth —
// Chesterton: the e2e consumes committed bytes, not a build step).

import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { mkdirSync, writeFileSync, rmSync, existsSync } from 'node:fs';
import { Buffer } from 'node:buffer';

// Same FileReader polyfill the sister fixture scripts use. GLTFExporter's
// data-URI emission path goes through FileReader.readAsDataURL even when
// `binary:false`, because each buffer is wrapped in a Blob.
class NodeFileReader {
  readAsDataURL(blob) {
    blob
      .arrayBuffer()
      .then((buf) => {
        const b64 = Buffer.from(buf).toString('base64');
        this.result = `data:application/octet-stream;base64,${b64}`;
        this.onload?.();
        this.onloadend?.();
      })
      .catch((err) => {
        this.onerror?.(err);
        this.onloadend?.();
      });
  }

  readAsArrayBuffer(blob) {
    blob
      .arrayBuffer()
      .then((buf) => {
        this.result = buf;
        this.onload?.();
        this.onloadend?.();
      })
      .catch((err) => {
        this.onerror?.(err);
        this.onloadend?.();
      });
  }
}
globalThis.FileReader = NodeFileReader;

import * as THREE from 'three';
import { GLTFExporter } from 'three/examples/jsm/exporters/GLTFExporter.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, '..');
const OUT_ROOT = resolve(ROOT, 'public/fixtures/multifile');

// --- the canonical "smallest valid PNG": 1×1 opaque red pixel. -----------
// PNG signature (8) + IHDR (25) + IDAT (~22) + IEND (12). Verified bytes;
// the renderer just needs a parseable PNG at the texture URI. Hand-coded
// so we don't pull in `canvas` / `sharp` / `jimp` as a dev-dep.
// REF: https://www.w3.org/TR/PNG/  (header, IHDR, IDAT zlib stream, IEND).
const ONE_PIXEL_RED_PNG = Buffer.from([
  // signature
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,
  // IHDR: length=13, type=IHDR, w=1, h=1, bitdepth=8, colortype=2(RGB),
  // compression=0, filter=0, interlace=0, crc
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xde,
  // IDAT: length=12, type=IDAT, zlib data (a single deflate block
  // containing the filter byte 0x00 + RGB bytes 0xff 0x00 0x00 = red), crc
  0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, 0x54, 0x08, 0xd7, 0x63, 0xf8, 0xcf, 0xc0, 0x00, 0x00,
  0x00, 0x03, 0x00, 0x01, 0x36, 0x00, 0xb5, 0x77,
  // IEND: length=0, type=IEND, crc
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82,
]);

// --- build a tiny textured mesh and export to .gltf JSON ------------------
async function exportBoxToGltfJson() {
  const scene = new THREE.Scene();
  const geometry = new THREE.BoxGeometry(1, 1, 1);
  // A textureless material is fine — the exporter won't emit an image, and
  // we inject the texture reference manually post-export. (Avoids fighting
  // three.js' Node-side texture loading for a data-URI source.)
  const mat = new THREE.MeshStandardMaterial({ color: '#cccccc', roughness: 0.8, metalness: 0 });
  const mesh = new THREE.Mesh(geometry, mat);
  mesh.name = 'Box';
  scene.add(mesh);
  const exporter = new GLTFExporter();
  return await new Promise((res, rej) => {
    exporter.parse(
      scene,
      (result) => {
        if (result instanceof ArrayBuffer) {
          rej(new Error('expected JSON for .gltf export, got ArrayBuffer'));
          return;
        }
        res(result);
      },
      (err) => rej(err),
      { binary: false },
    );
  });
}

// --- post-process: extract the data-URI buffer to scene.bin + rewrite uri,
//     and inject an external images[0] entry referencing the PNG sibling. --
function externalizeBufferAndAddImage(gltfJson, bufferUri, imageUri) {
  // The exporter emits buffers[0].uri as `data:application/octet-stream;base64,...`.
  // Extract that into raw bytes for an external `.bin` and rewrite the uri.
  const buf = gltfJson.buffers?.[0];
  if (!buf || typeof buf.uri !== 'string' || !buf.uri.startsWith('data:')) {
    throw new Error(`expected buffers[0].uri to be a data: URI from GLTFExporter, got ${buf?.uri}`);
  }
  const b64 = buf.uri.split(',', 2)[1];
  const binBytes = Buffer.from(b64, 'base64');
  buf.uri = bufferUri;

  // Inject an external image + texture + bind it to material[0] via the
  // baseColorTexture slot. The renderer's GLTFLoader walks images[].uri the
  // same way it walks buffers[].uri — both go through the LoadingManager's
  // URL modifier, which is the path Wave F's ingestion overrides for OPFS.
  gltfJson.images = [{ uri: imageUri, mimeType: 'image/png' }];
  gltfJson.samplers = gltfJson.samplers ?? [
    { magFilter: 9729, minFilter: 9729, wrapS: 10497, wrapT: 10497 },
  ];
  gltfJson.textures = [{ sampler: 0, source: 0 }];
  // Attach to material[0] (the box's standard material) baseColorTexture.
  const m = gltfJson.materials?.[0];
  if (!m) throw new Error('expected materials[0] in exporter output');
  m.pbrMetallicRoughness = m.pbrMetallicRoughness ?? {};
  m.pbrMetallicRoughness.baseColorTexture = { index: 0 };

  return { gltfJson, binBytes };
}

// --- variant writers ------------------------------------------------------
function writeVariant(dirAbs, files) {
  // files: { relPath: Buffer | string }. JSON is serialized by caller.
  mkdirSync(dirAbs, { recursive: true });
  for (const [relPath, bytes] of Object.entries(files)) {
    const full = resolve(dirAbs, relPath);
    mkdirSync(dirname(full), { recursive: true });
    writeFileSync(full, bytes);
  }
}

async function writeFlatVariant(baseGltf) {
  const { gltfJson, binBytes } = externalizeBufferAndAddImage(
    structuredClone(baseGltf),
    'scene.bin',
    'texture.png',
  );
  const outDir = resolve(OUT_ROOT, 'flat');
  writeVariant(outDir, {
    'scene.gltf': JSON.stringify(gltfJson, null, 2),
    'scene.bin': binBytes,
    'texture.png': ONE_PIXEL_RED_PNG,
  });

  // contract assertions
  if (!gltfJson.buffers[0].uri.endsWith('.bin')) {
    throw new Error(`flat: buffers[0].uri should end .bin, got ${gltfJson.buffers[0].uri}`);
  }
  if (!gltfJson.images[0].uri.endsWith('.png') || gltfJson.images[0].uri.startsWith('data:')) {
    throw new Error(`flat: images[0].uri should be external .png, got ${gltfJson.images[0].uri}`);
  }
  console.log(
    `flat/    buffers[0].uri=${gltfJson.buffers[0].uri}  images[0].uri=${gltfJson.images[0].uri}`,
  );
}

async function writeNestedVariant(baseGltf) {
  // The C1 trap: entry .gltf is NOT at the folder root. It lives at
  // nested/gltf/scene.gltf and addresses its siblings via `../`-relative
  // URIs. This proves the ingestion+renderer walk resolves a non-root entry.
  const { gltfJson, binBytes } = externalizeBufferAndAddImage(
    structuredClone(baseGltf),
    '../buffers/scene.bin',
    '../textures/texture.png',
  );
  const outDir = resolve(OUT_ROOT, 'nested');
  writeVariant(outDir, {
    'gltf/scene.gltf': JSON.stringify(gltfJson, null, 2),
    'buffers/scene.bin': binBytes,
    'textures/texture.png': ONE_PIXEL_RED_PNG,
  });

  // contract assertions
  if (!gltfJson.buffers[0].uri.startsWith('../')) {
    throw new Error(`nested: buffers[0].uri should start with ../, got ${gltfJson.buffers[0].uri}`);
  }
  if (!gltfJson.images[0].uri.startsWith('../')) {
    throw new Error(`nested: images[0].uri should start with ../, got ${gltfJson.images[0].uri}`);
  }
  console.log(
    `nested/  buffers[0].uri=${gltfJson.buffers[0].uri}  images[0].uri=${gltfJson.images[0].uri}`,
  );
}

async function writeSpacedVariant(baseGltf) {
  // Per glTF spec §3.9.3.1, URIs in the JSON are percent-encoded; the file
  // on disk carries the decoded form. The importer's `decodeURIComponent`
  // at opfsSiblingPath:128 hits this exact divergence, which is what Wave
  // F's percent-encoding gate verifies (Task 11).
  const { gltfJson, binBytes } = externalizeBufferAndAddImage(
    structuredClone(baseGltf),
    'scene.bin',
    'my%20texture.png', // percent-encoded space in the JSON URI
  );
  const outDir = resolve(OUT_ROOT, 'spaced');
  writeVariant(outDir, {
    'scene.gltf': JSON.stringify(gltfJson, null, 2),
    'scene.bin': binBytes,
    'my texture.png': ONE_PIXEL_RED_PNG, // literal space in the FILENAME
  });

  // contract assertions
  if (!gltfJson.images[0].uri.includes('%20')) {
    throw new Error(`spaced: images[0].uri should contain %20, got ${gltfJson.images[0].uri}`);
  }
  console.log(
    `spaced/  buffers[0].uri=${gltfJson.buffers[0].uri}  images[0].uri=${gltfJson.images[0].uri}`,
  );
}

async function main() {
  // Wipe + recreate the multifile/ subtree so re-runs are deterministic.
  if (existsSync(OUT_ROOT)) rmSync(OUT_ROOT, { recursive: true, force: true });
  mkdirSync(OUT_ROOT, { recursive: true });

  const baseGltf = await exportBoxToGltfJson();
  await writeFlatVariant(baseGltf);
  await writeNestedVariant(baseGltf);
  await writeSpacedVariant(baseGltf);

  console.log(`\nwrote 3 multi-file glTF variants under ${OUT_ROOT}`);
}

main().catch((err) => {
  console.error('gen-multifile-gltf-fixture failed:', err);
  process.exit(1);
});
